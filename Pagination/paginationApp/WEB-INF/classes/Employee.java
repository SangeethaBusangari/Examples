class Employee  
{
	int no;
	String name,email;
	public void setNo(int no)
	{this.no=no;}
	public void setName(String name)
	{
		this.name=name;
	}
	public void setEmail(String  mail)
	{
		email=mail;
	}
	public int getNo()
	{return no;}
	public String getName()
	{
		return name;
	}
	public String getEmail()
	{
		return email;
	}
}
